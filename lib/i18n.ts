export type Locale = "fi" | "en" | "sv" | "no" | "et";

export function isLocale(value: unknown): value is Locale {
  return value === "fi" || value === "en" || value === "sv" || value === "no" || value === "et";
}

export function applyLocale(nextLocale: Locale) {
  if (typeof window === "undefined") return;

  localStorage.setItem("locale", nextLocale);
  document.cookie = `locale=${nextLocale}; path=/; max-age=31536000; SameSite=Lax`;
  for (let index = 0; index < localStorage.length; index += 1) {
    const key = localStorage.key(index);
    if (key?.startsWith("visitor-language:")) {
      localStorage.setItem(key, nextLocale);
    }
  }
  document.documentElement.lang = nextLocale;
  removeLocaleSearchParam();
  window.dispatchEvent(new CustomEvent("localechange", { detail: nextLocale }));
}

function removeLocaleSearchParam() {
  const url = new URL(window.location.href);
  if (!url.searchParams.has("lang")) return;

  url.searchParams.delete("lang");
  window.history.replaceState(null, "", `${url.pathname}${url.search}${url.hash}`);
}

